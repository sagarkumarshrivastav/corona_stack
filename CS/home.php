        <h3>long will the coronavirus last?</h3>

        There's no way to tell how long the pandemic will continue. There are many factors, including the public’s
        efforts to slow the spread, researchers’ work to learn more about the virus, their search for a treatment, and
        the success of the vaccines.

        Symptoms of COVID-19
        The main symptoms include:

        Fever
        Coughing
        Shortness of breath
        Trouble breathing
        Fatigue
        Chills, sometimes with shaking
        Body aches
        Headache
        Sore throat
        Congestion/runny nose
        Loss of smell or taste
        Nausea
        Diarrhea
        The virus can lead to pneumonia, respiratory failure, heart problems, liver problems, septic shock, and death.
        Many COVID-19 complications may be caused by a condition known as cytokine release syndrome or a cytokine storm.
        This is when an infection triggers your immune system to flood your bloodstream with inflammatory proteins
        called cytokines. They can kill tissue and damage your organs.

        If you notice the following severe symptoms in yourself or a loved one, get medical help right away:

        Trouble breathing or shortness of breath
        Ongoing chest pain or pressure
        New confusion
        Can’t wake up fully
        Bluish lips or face
        Strokes have also been reported in some people who have COVID-19. Remember FAST:

        Face. Is one side of the person’s face numb or drooping? Is their smile lopsided?
        Arms. Is one arm weak or numb? If they try to raise both arms, does one arm sag?
        Speech. Can they speak clearly? Ask them to repeat a sentence.
        Time. Every minute counts when someone shows signs of a stroke. Call 911 right away.
        If you’re infected, symptoms can show up in as few as 2 days or as many as 14. It varies from person to person.

        According to researchers in China, these were the most common symptoms among people who had COVID-19:

        Fever 99%
        Fatigue 70%
        Cough 59%
        Lack of appetite 40%
        Body aches 35%
        Shortness of breath 31%
        Mucus/phlegm 27%
        Some people who are hospitalized for COVID-19 have also have dangerous blood clots, including in their legs,
        lungs, and arteries.

        What to do if you think you have it

        If you live in or have traveled to an area where COVID-19 is spreading:

        If you don’t feel well, stay home. Even if you have mild symptoms like a headache and runny nose, stay in until
        you’re better. This lets doctors focus on people who are more seriously ill and protects health care workers and
        people you might meet along the way. You might hear this called self-quarantine. Try to stay in a separate room
        away from other people in your home. Use a separate bathroom if you can.
        Call the doctor if you have trouble breathing. You need to get medical help as soon as possible. Calling ahead
        (rather than showing up) will let the doctor direct you to the proper place, which may not be your doctor’s
        office. If you don’t have a regular doctor, call your local board of health. They can tell you where to go for
        testing and treatment.
        Follow your doctor’s advice and keep up with the news on COVID-19. Between your doctor and health care
        authorities, you’ll get the care you need and information on how to prevent the virus from spreading.
        For more information about COVID-19, see our FAQ.

        How do I know if it’s COVID-19, a cold, or the flu?

        Symptoms of COVID-19 can be similar to a bad cold or the flu. Your doctor will suspect COVID-19 if:

        You have a fever and a cough.
        You have been exposed to people who have it within the last 14 days.
