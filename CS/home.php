        <h2 class="p2">Is there more than one strain of SARS-CoV-2?</h2>

        An early Chinese study of 103 COVID-19 cases found two strains, which they named L and S. The S type is older,
        but the L type was more common in early stages of the outbreak. They think one may cause more cases of the
        disease than the other, but they’re still working on what it all means.<br>

        It is also normal for a virus to change, or mutate, as it infects people and this virus has done so. There are
        several variants which have been named for the regions they were first discovered but they have now spread to
